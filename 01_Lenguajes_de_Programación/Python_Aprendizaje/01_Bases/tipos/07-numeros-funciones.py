import math

imaginario = 2 + 2j # 2 + 2i

print(abs(-5)) # Valor absoluto
print(round(3.6)) # Redondeo
print(pow(2, 3)) # Potencia
print(complex(2, 3)) # Número complejo  
print(divmod(7, 2)) # Cociente y resto
print(int(3.7)) # Conversión a entero   
print(float(5)) # Conversión a float

print(imaginario.real) # Parte real
print(imaginario.imag) # Parte imaginaria

print(math.sqrt(16)) # Raíz cuadrada
print(math.factorial(5)) # Factorial
print(math.gcd(12, 15)) # Máximo común divisor
print(math.sin(math.pi / 2)) # Seno
print(math.cos(0)) # Coseno
print(math.log(100, 10)) # Logaritmo base 10
print(math.exp(2)) # Exponencial e^x
print(math.ceil(4.2)) # Redondeo hacia arriba
print(math.floor(4.7)) # Redondeo hacia abajo
print(math.pi) # Constante pi
print(math.e) # Constante e
print(math.tan(math.pi / 4)) # Tangente
